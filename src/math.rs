pub fn cross(a:&[f32; 3], b:&[f32; 3]) -> [f32; 3] {
    [a[1]*b[2]-a[2]*b[1], a[2]*b[0]-b[2]*a[0], a[0]*b[1]-a[1]*b[0]]
}

pub fn normalize(v:&mut [f32;3]) -> [f32;3] {
    let len = (v[0] * v[0] + v[1] * v[1] + v[2] * v[2]).powf(0.5);
    v[0] = v[0] / len;
    v[1] = v[1] / len;
    v[2] = v[2] / len;
    *v
}

